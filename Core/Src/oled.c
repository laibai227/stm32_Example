/* OLED Display Gram - 128x64 bits = 1024 bytes */
static uint8_t OLED_Gram[8][128];

/* I2C handle - using hi2c1 for I2C1 */
extern I2C_HandleTypeDef hi2c1;

/* ==================== I2C Communication ==================== */

/**
 * @brief  Write command to OLED
 */
void OLED_Write_Command(uint8_t cmd)
{
    uint8_t tx_data[2] = {0x00, cmd};  /* 0x00 = control byte for command */
    HAL_I2C_Master_Transmit(&hi2c1, 0x78, tx_data, 2, 100);
}

/**
 * @brief  Write data buffer to OLED
 */
void OLED_Write_Data(uint8_t *data, uint8_t len)
{
    /* Use continuous write: control byte 0x40 then data */
    uint8_t tx_buf[len + 1];
    tx_buf[0] = 0x40;  /* Control byte for data */
    for (uint8_t i = 0; i < len; i++)
    {
        tx_buf[i + 1] = data[i];
    }
    HAL_I2C_Master_Transmit(&hi2c1, 0x78, tx_buf, len + 1, 200);
}

/**
 * @brief  Set cursor position
 */
void OLED_Set_Pos(uint8_t page, uint8_t col)
{
    OLED_Write_Command(0xB0 + page);
    OLED_Write_Command(0x00 | (col & 0x0F));
    OLED_Write_Command(0x10 | ((col >> 4) & 0x0F));
}

/**
 * @brief  Initialize OLED - hardware I2C
 */
void OLED_Init(void)
{
    /* Power-on delay */
    HAL_Delay(100);

    /* SSD1306 init sequence (from datasheet) */
    OLED_Write_Command(0xAE);  /* Display OFF */

    OLED_Write_Command(0xD5);  /* Set Display Clock Divide Ratio */
    OLED_Write_Command(0x80);

    OLED_Write_Command(0xA8);  /* Set Multiplex Ratio */
    OLED_Write_Command(0x3F);

    OLED_Write_Command(0xD3);  /* Set Display Offset */
    OLED_Write_Command(0x00);

    OLED_Write_Command(0x40);  /* Set Display Start Line */

    OLED_Write_Command(0xA1);  /* Set Segment Re-map (A1=normal, A0=mirror) */

    OLED_Write_Command(0xC8);  /* Set COM Output Scan Direction (C8=normal, C0=reverse) */

    OLED_Write_Command(0xDA);  /* Set COM Pins Hardware Config */
    OLED_Write_Command(0x12);

    OLED_Write_Command(0x81);  /* Set Contrast Control */
    OLED_Write_Command(0xCF);

    OLED_Write_Command(0xD9);  /* Set Pre-charge Period */
    OLED_Write_Command(0xF1);

    OLED_Write_Command(0xDB);  /* Set VCOMH Deselect Level */
    OLED_Write_Command(0x30);

    OLED_Write_Command(0xA4);  /* Entire Display ON (follow RAM) */

    OLED_Write_Command(0xA6);  /* Normal Display */

    OLED_Write_Command(0x8D);  /* Charge Pump Setting */
    OLED_Write_Command(0x14);

    OLED_Write_Command(0xAF);  /* Display ON */

    HAL_Delay(50);

    OLED_Clear();
}

/**
 * @brief  Clear display
 */
void OLED_Clear(void)
{
    uint8_t i, j;
    for (i = 0; i < 8; i++)
    {
        for (j = 0; j < 128; j++)
        {
            OLED_Gram[i][j] = 0;
        }
    }
    OLED_Refresh_Gram();
}

/**
 * @brief  Refresh display
 */
void OLED_Refresh_Gram(void)
{
    uint8_t i;
    for (i = 0; i < 8; i++)
    {
        OLED_Set_Pos(i, 0);
        OLED_Write_Data(OLED_Gram[i], 128);
    }
}

/**
 * @brief  Display on
 */
void OLED_Display_On(void)
{
    OLED_Write_Command(0xAF);
}

/**
 * @brief  Display off
 */
void OLED_Display_Off(void)
{
    OLED_Write_Command(0xAE);
}

/* ==================== Drawing Functions ==================== */

/**
 * @brief  Draw a point
 */
void OLED_Draw_Point(uint8_t x, uint8_t y, uint8_t bit)
{
    if (x >= 128 || y >= 64) return;
    if (bit)
        OLED_Gram[y >> 3][x] |= (1 << (y & 0x07));
    else
        OLED_Gram[y >> 3][x] &= ~(1 << (y & 0x07));
}

/**
 * @brief  Fill all
 */
void OLED_Fill(uint8_t fill_Data)
{
    uint8_t i, j;
    for (i = 0; i < 8; i++)
    {
        for (j = 0; j < 128; j++)
        {
            OLED_Gram[i][j] = fill_Data;
        }
    }
    OLED_Refresh_Gram();
}

