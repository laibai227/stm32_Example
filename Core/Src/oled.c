static uint32_t OLED_Pow(uint32_t x, uint32_t y)
{
    uint32_t r = 1;
    while (y--) r *= x;
    return r;
}

void OLED_ClearArea(uint8_t x, uint8_t y, uint8_t w, uint8_t h)
{
    for (uint8_t j = y; j < y + h && j < 64; j++)
        for (uint8_t i = x; i < x + w && i < 128; i++)
            OLED_Gram[j >> 3][i] &= ~(1 << (j & 0x07));
}

void OLED_Reverse(void)
{
    for (uint8_t j = 0; j < 8; j++)
        for (uint8_t i = 0; i < 128; i++)
            OLED_Gram[j][i] ^= 0xFF;
}

void OLED_ReverseArea(uint8_t x, uint8_t y, uint8_t w, uint8_t h)
{
    for (uint8_t j = y; j < y + h && j < 64; j++)
        for (uint8_t i = x; i < x + w && i < 128; i++)
            OLED_Gram[j >> 3][i] ^= (1 << (j & 0x07));
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size)
{
    uint8_t cw = (size == 16) ? 8 : 6;
    for (uint8_t i = 0; i < len; i++)
    {
        char ch = num / OLED_Pow(10, len - i - 1) % 10 + '0';
        if (size == 16) OLED_Show_Char_8x16(x + i * cw, y, ch);
        else OLED_Show_Char_6x8(x + i * cw, y, ch);
    }
}

void OLED_ShowSignedNum(uint8_t x, uint8_t y, int32_t num, uint8_t len, uint8_t size)
{
    uint8_t cw = (size == 16) ? 8 : 6;
    if (num >= 0)
    {
        if (size == 16) OLED_Show_Char_8x16(x, y, '+');
        else OLED_Show_Char_6x8(x, y, '+');
        OLED_ShowNum(x + cw, y, num, len, size);
    }
    else
    {
        if (size == 16) OLED_Show_Char_8x16(x, y, '-');
        else OLED_Show_Char_6x8(x, y, '-');
        OLED_ShowNum(x + cw, y, -num, len, size);
    }
}

void OLED_ShowHexNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size)
{
    uint8_t cw = (size == 16) ? 8 : 6;
    for (uint8_t i = 0; i < len; i++)
    {
        uint8_t s = num / OLED_Pow(16, len - i - 1) % 16;
        char ch = (s < 10) ? (s + '0') : (s - 10 + 'A');
        if (size == 16) OLED_Show_Char_8x16(x + i * cw, y, ch);
        else OLED_Show_Char_6x8(x + i * cw, y, ch);
    }
}

void OLED_ShowFloatNum(uint8_t x, uint8_t y, double num, uint8_t intLen, uint8_t fraLen, uint8_t size)
{
    uint8_t cw = (size == 16) ? 8 : 6;
    if (num >= 0)
    {
        if (size == 16) OLED_Show_Char_8x16(x, y, '+');
        else OLED_Show_Char_6x8(x, y, '+');
    }
    else
    {
        if (size == 16) OLED_Show_Char_8x16(x, y, '-');
        else OLED_Show_Char_6x8(x, y, '-');
        num = -num;
    }
    uint32_t intNum = (uint32_t)num;
    num -= intNum;
    uint32_t powN = OLED_Pow(10, fraLen);
    uint32_t fraNum = (uint32_t)(num * powN + 0.5);
    intNum += fraNum / powN;
    OLED_ShowNum(x + cw, y, intNum, intLen, size);
    if (size == 16) OLED_Show_Char_8x16(x + (intLen + 1) * cw, y, '.');
    else OLED_Show_Char_6x8(x + (intLen + 1) * cw, y, '.');
    OLED_ShowNum(x + (intLen + 2) * cw, y, fraNum, fraLen, size);
}

void OLED_Printf(uint8_t x, uint8_t y, uint8_t size, char *format, ...)
{
    char buf[128];
    va_list arg;
    va_start(arg, format);
    vsnprintf(buf, sizeof(buf), format, arg);
    va_end(arg);
    OLED_Show_String(x, y, buf, size);
}

void OLED_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    int16_t dx = abs(x1 - x0), dy = -abs(y1 - y0);
    int16_t sx = x0 < x1 ? 1 : -1, sy = y0 < y1 ? 1 : -1;
    int16_t err = dx + dy, e2;
    while (1)
    {
        OLED_Draw_Point(x0, y0, 1);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void OLED_DrawRectangle(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t filled)
{
    if (!filled)
    {
        for (uint8_t i = x; i < x + w; i++) { OLED_Draw_Point(i, y, 1); OLED_Draw_Point(i, y + h - 1, 1); }
        for (uint8_t i = y; i < y + h; i++) { OLED_Draw_Point(x, i, 1); OLED_Draw_Point(x + w - 1, i, 1); }
    }
    else
    {
        for (uint8_t i = x; i < x + w; i++)
            for (uint8_t j = y; j < y + h; j++)
                OLED_Draw_Point(i, j, 1);
    }
}

void OLED_DrawCircle(uint8_t x, uint8_t y, uint8_t r, uint8_t filled)
{
    int16_t cx = 0, cy = r, d = 1 - r;
    while (cx <= cy)
    {
        OLED_Draw_Point(x + cx, y + cy, 1);
        OLED_Draw_Point(x + cy, y + cx, 1);
        OLED_Draw_Point(x - cx, y + cy, 1);
        OLED_Draw_Point(x - cy, y + cx, 1);
        OLED_Draw_Point(x + cx, y - cy, 1);
        OLED_Draw_Point(x + cy, y - cx, 1);
        OLED_Draw_Point(x - cx, y - cy, 1);
        OLED_Draw_Point(x - cy, y - cx, 1);
        if (filled)
        {
            for (int16_t j = -cy; j <= cy; j++)
            {
                OLED_Draw_Point(x + cx, y + j, 1);
                OLED_Draw_Point(x - cx, y + j, 1);
            }
            for (int16_t j = -cx; j <= cx; j++)
            {
                OLED_Draw_Point(x + cy, y + j, 1);
                OLED_Draw_Point(x - cy, y + j, 1);
            }
        }
        cx++;
        if (d < 0) d += 2 * cx + 1;
        else { cy--; d += 2 * (cx - cy) + 1; }
    }
}

