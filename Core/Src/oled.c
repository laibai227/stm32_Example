/**
 * @brief  Show char - 6x8 font
 * @note   Font is column-major: 6 columns, 8 rows. Each byte is one column.
 *         bit 0 = top pixel, bit 7 = bottom pixel
 */
static void OLED_Show_Char_6x8(uint8_t x, uint8_t y, uint8_t chr)
{
    uint8_t i, j;
    const uint8_t *p = OLED_F6x8[chr - ' '];
    for (i = 0; i < 6; i++)  /* 6 columns */
    {
        uint8_t col_data = p[i];
        for (j = 0; j < 8; j++)  /* 8 rows */
        {
            if (col_data & 0x01)
                OLED_Draw_Point(x + i, y + j, 1);
            else
                OLED_Draw_Point(x + i, y + j, 0);
            col_data >>= 1;
        }
    }
}

/**
 * @brief  Show char - 8x16 font
 * @note   Font is column-major: 8 columns, 16 rows
 *         Bytes 0-7: top half (rows 0-7)
 *         Bytes 8-15: bottom half (rows 8-15)
 *         bit 7 = left pixel, bit 0 = right pixel (MSB first for PCtoLCD format)
 *         When drawing, we shift right so bit 0 goes to bit 0 position
 */
static void OLED_Show_Char_8x16(uint8_t x, uint8_t y, uint8_t chr)
{
    uint8_t i, j;
    const uint8_t *p = OLED_F8x16[chr - ' '];
    for (i = 0; i < 8; i++)  /* 8 columns */
    {
        /* Top half: bytes 0-7, MSB (bit 7) = top row */
        uint8_t col_data = p[i];
        for (j = 0; j < 8; j++)  /* 8 rows */
        {
            if (col_data & 0x80)
                OLED_Draw_Point(x + i, y + j, 1);
            else
                OLED_Draw_Point(x + i, y + j, 0);
            col_data <<= 1;
        }
        /* Bottom half: bytes 8-15 */
        col_data = p[i + 8];
        for (j = 0; j < 8; j++)  /* 8 rows */
        {
            if (col_data & 0x80)
                OLED_Draw_Point(x + i, y + 8 + j, 1);
            else
                OLED_Draw_Point(x + i, y + 8 + j, 0);
            col_data <<= 1;
        }
    }
}

/**
 * @brief  Show string
 */
void OLED_Show_String(uint8_t x, uint8_t y, char *str, uint8_t size)
{
    uint8_t i = 0;
    uint8_t char_width = (size == 16) ? 8 : 6;

    while (str[i] != '\0')
    {
        if (size == 16)
            OLED_Show_Char_8x16(x + i * char_width, y, str[i]);
        else
            OLED_Show_Char_6x8(x + i * char_width, y, str[i]);
        i++;
        if (x + i * char_width >= 128) break;
    }
}

